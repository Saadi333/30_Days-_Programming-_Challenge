#include <stdio.h>

int main() {
  // Loop variable to iterate through numbers
  int number;

  // Loop from 1 to 100 (inclusive)
  for (number = 1; number <= 100; number++) {
    // Print the current number
    printf("%d ", number);
  }

  // Add a newline after printing all numbers
  printf("\n");

  return 0;
}
